def _ioi_from_resp_times(resp_times_s):
    if len(resp_times_s) < 2: return np.array([])
    return np.diff(np.asarray(resp_times_s)) * 1000.0

def _velocidad_desde_df(df, fps, wl, polyorder=3, col_y='tip_y', escala_final=1.0):
    y = df[col_y].ffill().bfill().values
    if wl % 2 == 0: wl += 1
    if wl <= polyorder: wl = polyorder + 2
    return savgol_filter(y, wl, polyorder, deriv=1, delta=1/fps) * escala_final

def _aceleracion_desde_df(df, fps, wl, polyorder=3, col_y='tip_y', escala_final=1.0):
    y = df[col_y].ffill().bfill().values
    if wl % 2 == 0: wl += 1
    if wl <= polyorder: wl = polyorder + 2
    return savgol_filter(y, wl, polyorder, deriv=2, delta=1/fps) * escala_final


