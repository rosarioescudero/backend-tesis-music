def _calcular_asincronia_greedy_audio(metro_peaks, tapping_peaks, bpm, sample_rate):
    """
    [V6-7] Algoritmo greedy de emparejamiento pico a pico en muestras de audio.
    Replicado fielmente del notebook 05_11 (sección AUDIO).

    Para cada tapping_peak, busca el metronomo_peak más cercano; si la diferencia
    en ms es menor a match_ms = ½ período, los empareja y elimina el metro_peak
    usado. Funciona para ISO, PA y PR (un tramo a la vez).

    Args:
        metro_peaks   : array de índices (muestras) de picos del metrónomo.
        tapping_peaks : array de índices (muestras) de picos de tapping.
        bpm           : BPM de referencia del tramo (define match_ms).
        sample_rate   : Hz.
    Returns:
        asynchrony_ms         : np.ndarray de asincronías [ms] (signed).
        metro_peaks_matched   : np.ndarray de índices de metrónomo emparejados.
        tapping_peaks_matched : np.ndarray de índices de tapping emparejados.
    """
    match_ms = (60.0 / bpm) * 1000.0 / 2.0
    metro_temp = list(metro_peaks)
    asynchrony_ms, metro_matched, tapping_matched = [], [], []

    for tap in tapping_peaks:
        if not metro_temp:
            break
        diffs        = np.abs(tap - np.array(metro_temp))
        idx_cercano  = np.argmin(diffs)
        metro_cercano = metro_temp[idx_cercano]
        asinc_ms = (tap - metro_cercano) * 1000.0 / sample_rate

        if abs(asinc_ms) < match_ms:
            asynchrony_ms.append(asinc_ms)
            metro_matched.append(metro_cercano)
            tapping_matched.append(tap)
            del metro_temp[idx_cercano]

    return (np.array(asynchrony_ms),
            np.array(metro_matched),
            np.array(tapping_matched))


