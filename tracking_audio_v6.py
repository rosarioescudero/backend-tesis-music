def detect_peaks(senal, prominence=None, height=None, distance=None):
    """[C-5.2] Función canónica única para detección de picos en señal 1D."""
    peaks, _ = find_peaks(senal, prominence=prominence, height=height,
                          distance=distance)
    return peaks


def _calcular_tiempo_rampa(bpm1, bpm2, n_clics):
    """[C-7.3] Duración exacta de la rampa sumando períodos interpolados."""
    if n_clics <= 1:
        return 0.0
    bpms = np.linspace(bpm1, bpm2, n_clics)
    return float(np.sum(60.0 / bpms[:-1]))


def convert_numpy_to_python(obj):
    """[C-9.5] Convierte tipos NumPy a Python nativos. NaN/inf → None."""
    if isinstance(obj, np.ndarray):
        return [convert_numpy_to_python(v) for v in obj.tolist()]
    elif isinstance(obj, np.integer): return int(obj)
    elif isinstance(obj, np.floating):
        val = float(obj)
        return None if (np.isnan(val) or np.isinf(val)) else val
    elif isinstance(obj, np.bool_): return bool(obj)
    elif isinstance(obj, dict):
        return {k: convert_numpy_to_python(v) for k, v in obj.items()}
    elif isinstance(obj, (list, tuple)):
        return [convert_numpy_to_python(v) for v in obj]
    elif isinstance(obj, float):
        return None if (np.isnan(obj) or np.isinf(obj)) else obj
    return obj


def calcular_estadisticas(arr_ms, nombre=""):
    """Estadísticas descriptivas de un array de valores [ms]."""
    a = np.asarray(arr_ms, dtype=float)
    a = a[~np.isnan(a)]
    if a.size == 0: return {}
    return {
        'mean_ms':   float(np.mean(a)),
        'std_ms':    float(np.std(a)),
        'median_ms': float(np.median(a)),
        'min_ms':    float(np.min(a)),
        'max_ms':    float(np.max(a)),
        'n':         int(a.size)
    }


def graficar_cambios_bpm(metadata, start_time=0, ax=None):
    """
    [C-10.3] Dibuja líneas verticales de cambio de BPM sobre ax.
    Retorna float (PA) | tuple[float,float] (PR) | None (ISO).
    """
    tipo = metadata.get('tipo_metronomo', 'ISO')
    if tipo == 'PA':
        cambio = metadata['clics_1'] * (60.0 / metadata['bpm_1']) + start_time
        if GRAFICAR and ax is not None:
            ax.axvline(x=cambio, color='r', linestyle='--', label='Cambio de BPM')
        return cambio
    elif tipo == 'PR':
        cambio1 = metadata['clics_1'] * (60.0 / metadata['bpm_1']) + start_time
        tr      = _calcular_tiempo_rampa(metadata['bpm_1'], metadata['bpm_2'],
                                         metadata['clics_rampa'])
        cambio2 = cambio1 + tr
        if GRAFICAR and ax is not None:
            ax.axvline(x=cambio1, color='r', linestyle='--', label='1er cambio BPM')
            ax.axvline(x=cambio2, color='g', linestyle='--', label='2do cambio BPM')
        return cambio1, cambio2
    return None


