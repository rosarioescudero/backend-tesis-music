    t_sec = df['frame'] / fps
    fig_insp, ax_insp = plt.subplots(figsize=(16, 5))
    ax_insp.plot(t_sec, df['tip_y'], label='Señal Cruda tip_y',
                 color='purple', alpha=0.6, linewidth=1.5)
    ax_insp.scatter(t_sec.iloc[tip_peaks_a], df['tip_y'].iloc[tip_peaks_a],
                    color='cyan', s=60, edgecolors='black', zorder=5,
                    label=f'Picos Aire (n={len(tip_peaks_a)})')
    ax_insp.scatter(t_sec.iloc[tip_peaks_b], df['tip_y'].iloc[tip_peaks_b],
                    color='red', s=60, edgecolors='black', zorder=5,
                    label=f'Impactos Mesa (n={len(tip_peaks_b)})')
    ax_insp.invert_yaxis()
    graficar_cambios_bpm(metadata, start_time, ax=ax_insp)   # [V6-11]
    ax_insp.set_title(f'INSPECCIÓN VISUAL TIP_Y — {filename_stem}', fontweight='bold')
    ax_insp.set_xlabel('Tiempo de Video [s]')
    ax_insp.set_ylabel('Posición Y Normalizada (MediaPipe) — invertida')
    ax_insp.legend(loc='upper right'); ax_insp.grid(True, linestyle='--', alpha=0.7)
    plt.tight_layout()
    nombre_tip_y = f"{filename_stem}_inspeccion_tip_y.png"
    ruta_tip_y   = os.path.join(OUTPUT_DIR, nombre_tip_y)
    plt.savefig(ruta_tip_y, dpi=300, bbox_inches='tight')
    print(f"\n[!] Gráfico de inspección guardado: {ruta_tip_y}")
    _mostrar_grafico()
    plt.close(fig_insp)

    # --- beats_antes_reaccion ---
    pico_bajo_inicial_idx = tip_peaks_b[0] if len(tip_peaks_b) > 0 else 0
    primer_reaccion       = pico_bajo_inicial_idx / fps
    beats_antes_reaccion  = int(np.sum(stim_times < primer_reaccion))
    metadata['beats_antes_reaccion'] = beats_antes_reaccion
    print(f"Primer movimiento en t={primer_reaccion:.2f}s | "
          f"Beats antes de reaccionar: {beats_antes_reaccion}")

    # resp_times desde tip_peaks_b / fps, referenciados desde el inicio del metrónomo
    resp_times_video = np.array([idx / fps for idx in tip_peaks_b])
    resp_times_s     = resp_times_video - start_time
    resp_times_s     = resp_times_s[resp_times_s >= 0]

    # ==========================================================================
    # 13. SEÑAL IDEAL
    # ==========================================================================
    df = generar_senal_ideal(df, metadata, fps)

    # ==========================================================================
    # 14. AMPLITUD Y TIEMPOS DE ASCENSO/DESCENSO
    # ==========================================================================
    cambios = graficar_cambios_bpm(metadata, start_time, ax=None)

    amplitudes_por_tramo  = _calcular_amplitudes_por_tramo(
        df, tip_peaks_a, tip_peaks_b, metadata, fps, cambios)
    tiempos_por_tramo     = _calcular_tiempos_ascenso_descenso(
        df, tip_peaks_a, tip_peaks_b, metadata, fps, cambios)
    tiempo_apoyo_ms, tiempo_alzada_ms = _calcular_tiempos_apoyo_alzada(
        df, tip_peaks_a, tip_peaks_b, fps)

    if GRAFICAR:
        fig, ax = plt.subplots(figsize=(15, 5))
        ax.plot(df['frame'] / fps, df['tip_y_cm'], label='TIP (cm)', color='orange')
        ax.scatter(df['frame'][tip_peaks_a] / fps, df['tip_y_cm'][tip_peaks_a],
                   color='cyan', label='Alzadas (aire)')
        ax.scatter(df['frame'][tip_peaks_b] / fps, df['tip_y_cm'][tip_peaks_b],
                   color='green', label='Impactos mesa')
        graficar_cambios_bpm(metadata, start_time, ax=ax)
        ax.set_xlabel('Tiempo [s]'); ax.set_ylabel('Posición TIP [cm]')
        ax.set_title(f'{filename_stem} — TIP en cm')
        ax.legend(); ax.grid(True); plt.tight_layout(); _mostrar_grafico()

        t_desc_arr, t_asc_arr = _calcular_tiempos_asc_desc_serie(tip_peaks_a, tip_peaks_b, fps)
        _graficar_tiempos_asc_desc(t_desc_arr, t_asc_arr, metadata, filename_stem)

    # ==========================================================================
    # 15. CINEMÁTICA
    # ==========================================================================
    bpm_cinem    = metadata.get('bpm', metadata.get('bpm_1', BPM_BASE))
    ciclo_frames = max(1, int(fps * 60.0 / bpm_cinem))
    wl           = max(SAVGOL_POLYORDER + 2, ciclo_frames // 3)
    if wl % 2 == 0: wl += 1
    print(f"\nSavitzky-Golay: window_length={wl} | polyorder={SAVGOL_POLYORDER}")

    sm_r, ha_r, cu_r = calcular_metricas_movimiento(df, 'tip',   fps, wl, SAVGOL_POLYORDER)
    sm_i, ha_i, cu_i = calcular_metricas_movimiento(df, 'ideal', fps, wl, SAVGOL_POLYORDER)

    def _ratio(a, b):
        return round(float(a) / float(b), 4) if b and b > 1e-10 else None

    print(f"  Suavidad  real/ideal: {sm_r:.4f}/{sm_i:.4f} → {_ratio(sm_r, sm_i)}")
    print(f"  Armonía   real/ideal: {ha_r:.4f}/{ha_i:.4f} → {_ratio(ha_r, ha_i)}")
    print(f"  Curvatura real/ideal: {cu_r:.4f}/{cu_i:.4f} → {_ratio(cu_r, cu_i)}")

