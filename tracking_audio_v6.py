# ALGORITMO MATRICIAL DE ASINCRONÍA — VIDEO (compute_asyn, Repp 2005)
# ==============================================================================

def compute_asyn(stim_times, resp_times, ISI):
    """
    [N-4] Asignación bidireccional estímulo-respuesta (Repp, 2005).
    Usada exclusivamente para la asincronía de VIDEO (eje ISO).
    """
    if len(resp_times) == 0:
        return pd.DataFrame({'asyn': [], 'assigned_stim': []})
    asyn_max = 0.5 * ISI
    n_s, n_r  = len(stim_times), len(resp_times)
    asyn      = np.full(n_r, np.nan)
    assigned  = -np.ones(n_r, dtype=int)
    diffs     = -np.subtract.outer(stim_times, resp_times).astype(float)
    diffs[np.abs(diffs) >= asyn_max] = np.nan

    minR = np.zeros((n_s, n_r), dtype=int)
    for r in range(n_r):
        col = diffs[:, r]
        if not np.all(np.isnan(col)):
            m = np.nanmin(np.abs(col))
            minR[np.where(np.abs(col) == m), r] = 1

    minS = np.zeros((n_s, n_r), dtype=int)
    for s in range(n_s):
        row = diffs[s, :]
        if not np.all(np.isnan(row)):
            m = np.nanmin(np.abs(row))
            minS[s, np.where(np.abs(row) == m)] = 1

    def _quitar_ambiguos(mat):
        shift = mat + np.roll(mat, (0, 1), (0, 1)) + np.roll(mat, (0, -1), (0, 1))
        mat[shift >= 2] = 0
        return mat

    inter  = _quitar_ambiguos(minR) * _quitar_ambiguos(minS)
    S_idx, R_idx = np.where(inter == 1)
    assigned[R_idx] = S_idx
    if S_idx.size > 0:
        lin_ind   = np.ravel_multi_index((S_idx, R_idx), diffs.shape)
        asyn[R_idx] = diffs.ravel()[lin_ind] * 1000.0
    return pd.DataFrame({'asyn': asyn, 'assigned_stim': assigned})


