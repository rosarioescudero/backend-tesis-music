    # 11. ESTÍMULOS Y PICOS DEL METRÓNOMO
    # ==========================================================================
    tipo    = metadata.get('tipo_metronomo', 'ISO')
    bpm_ref = metadata.get('bpm', metadata.get('bpm_1', BPM_BASE))

    stim_times, peaks_por_tramo = _construir_stim_times(metronome_clean, metadata, sample_rate)
