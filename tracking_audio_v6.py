def _construir_stim_times(metronome_clean, metadata, sample_rate):
    """[N-8] Construye el array de tiempos de estímulos [s] para ISO, PA y PR."""
    tipo = metadata.get('tipo_metronomo', 'ISO')

    if tipo == 'ISO':
        bpm   = metadata['bpm']
        peaks = detect_peaks(metronome_clean,
                             distance=int(sample_rate * (60.0 / bpm - 0.1)))
        return peaks / sample_rate, {'iso': peaks.tolist()}

    elif tipo == 'PA':
        bpm_1, bpm_2 = metadata['bpm_1'], metadata['bpm_2']
        tc = metadata['clics_1'] * (60.0 / bpm_1)
        p1 = detect_peaks(metronome_clean[:int((tc - 0.15) * sample_rate)],
                          distance=int(sample_rate * (60.0 / bpm_1 - 0.1)))
        p2 = detect_peaks(metronome_clean[int(tc * sample_rate):],
                          distance=int(sample_rate * (60.0 / bpm_2 - 0.1)))
        return (np.concatenate([p1 / sample_rate, p2 / sample_rate + tc]),
                {'pa_1': p1.tolist(), 'pa_2': p2.tolist(), 'tc': tc})

    elif tipo == 'PR':
        bpm_1, bpm_2 = metadata['bpm_1'], metadata['bpm_2']
        dur_1 = metadata['clics_1'] * (60.0 / bpm_1)
        dur_r = _calcular_tiempo_rampa(bpm_1, bpm_2, metadata['clics_rampa'])
        t_rf  = dur_1 + dur_r
        d_min = int(sample_rate * (60.0 / max(bpm_1, bpm_2) - 0.1))
        p_all = detect_peaks(metronome_clean, distance=d_min)
        pt    = p_all / sample_rate
        p1    = p_all[pt <= dur_1]
        p_r   = p_all[(pt > dur_1) & (pt <= t_rf)]
        p2    = p_all[pt > t_rf]
        return (np.concatenate([p1 / sample_rate, p_r / sample_rate, p2 / sample_rate]),
                {'pr_1': p1.tolist(), 'pr_rampa': p_r.tolist(), 'pr_2': p2.tolist()})

    return np.array([]), {}


