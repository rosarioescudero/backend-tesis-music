def transform_to_iso(stim_times, target_bpm):
    """[N-5] Convierte el eje temporal PA/PR a ISO constante."""
    target_isi     = 60.0 / target_bpm
    stim_times_iso = stim_times[0] + np.arange(len(stim_times)) * target_isi
    return stim_times_iso, target_isi


def map_resp_to_iso(resp_times, stim_times_orig, stim_times_iso):
    """[N-6] Proyecta resp_times al eje ISO con extrapolación lineal en bordes."""
    resp_times = np.asarray(resp_times)
    stim_times_orig = np.asarray(stim_times_orig)
    stim_times_iso  = np.asarray(stim_times_iso)
    resp_iso = np.interp(resp_times, stim_times_orig, stim_times_iso)
    mask_left = resp_times < stim_times_orig[0]
    if np.any(mask_left) and len(stim_times_orig) >= 2:
        slope = ((stim_times_iso[1] - stim_times_iso[0]) /
                 (stim_times_orig[1] - stim_times_orig[0]))
        resp_iso[mask_left] = stim_times_iso[0] + slope * (resp_times[mask_left] - stim_times_orig[0])
    mask_right = resp_times > stim_times_orig[-1]
    if np.any(mask_right) and len(stim_times_orig) >= 2:
        slope = ((stim_times_iso[-1] - stim_times_iso[-2]) /
                 (stim_times_orig[-1] - stim_times_orig[-2]))
        resp_iso[mask_right] = stim_times_iso[-1] + slope * (resp_times[mask_right] - stim_times_orig[-1])
    n_l, n_r = int(np.sum(mask_left)), int(np.sum(mask_right))
    if n_l: print(f"  [map_resp_to_iso] {n_l} resp. extrapoladas ANTES del primer estímulo.")
    if n_r: print(f"  [map_resp_to_iso] {n_r} resp. extrapoladas DESPUÉS del último estímulo.")
    return resp_iso


def revert_to_original(iso_times, stim_times_iso, stim_times_orig):
    """
    [V6-5] Invierte el mapeo ISO → tiempo real del video.
    Usa interpolación lineal con extrapolación en los bordes.
    Si la transformación es biyectiva, el error de inversión debe ser < 1 µs.
    """
    iso_times       = np.asarray(iso_times)
    stim_times_iso  = np.asarray(stim_times_iso)
    stim_times_orig = np.asarray(stim_times_orig)
    revertidos = np.interp(iso_times, stim_times_iso, stim_times_orig)
    mask_left = iso_times < stim_times_iso[0]
    if np.any(mask_left) and len(stim_times_iso) >= 2:
        slope = ((stim_times_orig[1] - stim_times_orig[0]) /
                 (stim_times_iso[1]  - stim_times_iso[0]))
        revertidos[mask_left] = stim_times_orig[0] + slope * (iso_times[mask_left] - stim_times_iso[0])
    mask_right = iso_times > stim_times_iso[-1]
    if np.any(mask_right) and len(stim_times_iso) >= 2:
        slope = ((stim_times_orig[-1] - stim_times_orig[-2]) /
                 (stim_times_iso[-1]  - stim_times_iso[-2]))
        revertidos[mask_right] = stim_times_orig[-1] + slope * (iso_times[mask_right] - stim_times_iso[-1])
    return revertidos


