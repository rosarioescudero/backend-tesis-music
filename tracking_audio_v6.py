    # ==========================================================================
    # 16. ASINCRONÍA DESDE VIDEO — ISO MAPPING + compute_asyn [V6-1]
    # ==========================================================================
    print(f"\n{'='*70}")
    print(f"SECCIÓN 16: ASINCRONÍA DESDE VIDEO (eje ISO)")
    print(f"{'='*70}")

    graficos = {nombre_tip_y: nombre_tip_y}

    # a) Transformar estímulos al eje ISO
    stim_times_iso, target_isi_calc = transform_to_iso(stim_times, TARGET_BPM_ISO)
    print(f"Estímulos transformados a ISO ({TARGET_BPM_ISO} BPM) | ISI={target_isi_calc:.4f}s")
