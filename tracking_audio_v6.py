# [V5-1, V5-2] DETECCIÓN DE VALLES — ALGORITMO DE FRAN
# ==============================================================================

def detect_valley_center(signal_values, peak_a_idx, next_peak_a_idx, n_local=5):
    """
    [V5-1] Algoritmo de Fran: centro de meseta entre dos picos altos consecutivos.
    Toma los n_local mínimos locales más bajos, luego calcula el punto medio
    temporal entre el más a la izquierda y el más a la derecha de ese conjunto.
    """
    segment = signal_values[peak_a_idx:next_peak_a_idx]
    if len(segment) < 3:
        return peak_a_idx + int(np.argmin(segment))
    local_mins, _ = find_peaks(-segment, prominence=0)
    if len(local_mins) == 0:
        return peak_a_idx + int(np.argmin(segment))
    n = min(n_local, len(local_mins))
    lowest_n_idx = local_mins[np.argsort(segment[local_mins])[:n]]
    if len(lowest_n_idx) == 1:
        return peak_a_idx + int(lowest_n_idx[0])
    extremo_izq = lowest_n_idx[np.argmin(lowest_n_idx)]
    extremo_der = lowest_n_idx[np.argmax(lowest_n_idx)]
    return peak_a_idx + int(round((extremo_izq + extremo_der) / 2))


def _construir_tip_peaks_b(df, tip_peaks_a, fps, bpm_ref, n_local=5):
    """
    [V5-2] Construye tip_peaks_b completo usando detect_valley_center.
    Incluye pico bajo inicial (ventana pre-primer-alto) y pico bajo final.
    """
    signal_values = df['tip_y'].values
    n_total       = len(signal_values)

    if len(tip_peaks_a) > 0:
        primer_alto = tip_peaks_a[0]
        ventana     = max(3, int(fps * 60.0 / bpm_ref))
        inicio_seg  = max(0, primer_alto - ventana)
        seg_inicial = signal_values[inicio_seg:primer_alto]
        pico_bajo_inicial_idx = inicio_seg + int(np.argmin(seg_inicial)) \
                                if len(seg_inicial) > 0 else 0
    else:
        pico_bajo_inicial_idx = 0

    tip_peaks_b = []
    for i in range(len(tip_peaks_a) - 1):
        tip_peaks_b.append(detect_valley_center(
            signal_values, tip_peaks_a[i], tip_peaks_a[i + 1], n_local=n_local))

    if len(tip_peaks_a) > 0:
        fin_seg = min(n_total - 1, tip_peaks_a[-1] + max(3, int(fps * 60.0 / bpm_ref)))
        tip_peaks_b.append(detect_valley_center(
            signal_values, tip_peaks_a[-1], fin_seg, n_local=n_local))

    return np.array(tip_peaks_b, dtype=int), pico_bajo_inicial_idx


