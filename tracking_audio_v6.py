def calcular_alpha_correccion(asinc_ms):
    a = np.asarray(asinc_ms, dtype=float); a = a[~np.isnan(a)]
    if len(a) < 3:
        return {'alpha': None, 'intercept': None, 'r_squared': None, 'n': int(len(a))}
    slope, intercept, r_value, p_value, _ = linregress(a[:-1], a[1:])
    return {'alpha': round(float(slope), 4), 'intercept': round(float(intercept), 3),
            'r_squared': round(float(r_value**2), 4), 'p_value': round(float(p_value), 4),
            'n': int(len(a)-1)}

def calcular_tiempo_restablecimiento(asinc_ms, stim_times_s, t_perturbacion_s,
                                      ventana_baseline_n=10, sigma_factor=1.0):
    a, t = np.asarray(asinc_ms, dtype=float), np.asarray(stim_times_s, dtype=float)
    if len(a) != len(t) or len(a) < ventana_baseline_n + 3:
        return {'Tr_beats': None, 'Tr_ms': None, 'mu_baseline_ms': None,
                'sigma_baseline_ms': None, 'idx_perturbacion': None}
    idx_pert = int(np.searchsorted(t, t_perturbacion_s))
    if idx_pert < ventana_baseline_n: idx_pert = ventana_baseline_n
    if idx_pert >= len(a):
        return {'Tr_beats': None, 'Tr_ms': None, 'mu_baseline_ms': None,
                'sigma_baseline_ms': None, 'idx_perturbacion': idx_pert}
    baseline = a[max(0, idx_pert - ventana_baseline_n): idx_pert]
    baseline = baseline[~np.isnan(baseline)]
    if len(baseline) < 3:
        return {'Tr_beats': None, 'Tr_ms': None, 'mu_baseline_ms': None,
                'sigma_baseline_ms': None, 'idx_perturbacion': idx_pert}
    mu_b, sigma_b = float(np.mean(baseline)), float(np.std(baseline))
    lim_inf, lim_sup = mu_b - sigma_factor * sigma_b, mu_b + sigma_factor * sigma_b
    post = a[idx_pert:]; en_rango = (post >= lim_inf) & (post <= lim_sup)
    Tr_beats = None
    for k in range(len(en_rango) - 2):
        if en_rango[k] and en_rango[k+1] and en_rango[k+2]:
            Tr_beats = k + 1; break
    Tr_ms = None
    if Tr_beats is not None and idx_pert + Tr_beats < len(t):
        Tr_ms = float((t[idx_pert + Tr_beats] - t[idx_pert]) * 1000.0)
    return {'Tr_beats': Tr_beats, 'Tr_ms': round(Tr_ms, 2) if Tr_ms else None,
            'mu_baseline_ms': round(mu_b, 3), 'sigma_baseline_ms': round(sigma_b, 3),
            'idx_perturbacion': idx_pert}

def calcular_pendiente_adaptacion(asinc_ms, stim_times_s, t_rampa_ini_s, t_rampa_fin_s):
    a, t = np.asarray(asinc_ms, dtype=float), np.asarray(stim_times_s, dtype=float)
    mask = (t >= t_rampa_ini_s) & (t <= t_rampa_fin_s) & ~np.isnan(a)
    if np.sum(mask) < 3:
        return {'slope_ms_per_s': None, 'r_squared': None,
                'n': int(np.sum(mask)), 'mean_asinc_rampa_ms': None}
    t_r, a_r = t[mask], a[mask]
    slope, _, r_value, p_value, _ = linregress(t_r, a_r)
    return {'slope_ms_per_s': round(float(slope), 4), 'r_squared': round(float(r_value**2), 4),
            'p_value': round(float(p_value), 4), 'n': int(np.sum(mask)),
            'mean_asinc_rampa_ms': round(float(np.mean(a_r)), 3)}

def calcular_indice_acentuacion(df, stim_times_s, fps, escala_final,
                                  wl, polyorder=3, ventana_pre_ms=100.0, col_y='tip_y'):
    vy = _velocidad_desde_df(df, fps, wl, polyorder, col_y, escala_final)
    t_f = df['frame'].values / fps
    v_media = float(np.mean(np.abs(vy))) or 1.0
    ventana_s = ventana_pre_ms / 1000.0
    AI_list = []
    for t_beat in stim_times_s:
        mask = (t_f >= (t_beat - ventana_s)) & (t_f <= t_beat)
        if np.sum(mask) == 0: continue
        v_pre = float(np.mean(np.abs(vy[mask])))
        AI = v_pre / v_media if v_media > 1e-8 else np.nan
        if not np.isnan(AI): AI_list.append(AI)
    if not AI_list:
        return {'AI_mean': None, 'AI_std': None, 'AI_por_beat': [],
                'v_media_global_cm_s': round(v_media, 4)}
    return {'AI_mean': round(float(np.mean(AI_list)), 4), 'AI_std': round(float(np.std(AI_list)), 4),
            'AI_por_beat': [round(v, 4) for v in AI_list], 'v_media_global_cm_s': round(v_media, 4)}

def calcular_adelanto_fase(df, stim_times_s, fps, escala_final, wl,
                             polyorder=3, ventana_busqueda_ms=200.0, col_y='tip_y'):
    vy = _velocidad_desde_df(df, fps, wl, polyorder, col_y, escala_final)
    t_f = df['frame'].values / fps
    ventana_s = ventana_busqueda_ms / 1000.0
    phi_list = []
    for t_beat in stim_times_s:
        mask = (t_f >= (t_beat - ventana_s)) & (t_f <= (t_beat + ventana_s))
        if np.sum(mask) == 0: continue
        idx_v = np.where(mask)[0]
        phi_list.append((t_f[idx_v[np.argmax(np.abs(vy[idx_v]))]] - t_beat) * 1000.0)
    if not phi_list:
        return {'phi_mean_ms': None, 'phi_std_ms': None, 'phi_por_beat_ms': [], 'n': 0}
    return {'phi_mean_ms': round(float(np.mean(phi_list)), 3),
            'phi_std_ms': round(float(np.std(phi_list)), 3),
            'phi_por_beat_ms': [round(v, 3) for v in phi_list], 'n': int(len(phi_list))}

def calcular_cpt(df, stim_times_s, asinc_ms, fps, escala_final, wl,
                  polyorder=3, ventana_pre_ms=150.0, col_y='tip_y'):
    ay = _aceleracion_desde_df(df, fps, wl, polyorder, col_y, escala_final)
    t_f = df['frame'].values / fps
    a = np.asarray(asinc_ms, dtype=float)
    ventana_s = ventana_pre_ms / 1000.0
    accel_pre_list, asinc_next_list = [], []
    for i, t_beat in enumerate(stim_times_s[:-1]):
        mask = (t_f >= (t_beat - ventana_s)) & (t_f <= t_beat)
        if np.sum(mask) == 0: continue
        a_next = a[i + 1] if i + 1 < len(a) else np.nan
        if not np.isnan(a_next):
            accel_pre_list.append(float(np.mean(np.abs(ay[mask]))))
            asinc_next_list.append(a_next)
    if len(accel_pre_list) < 5:
        return {'CPT_r': None, 'CPT_p': None, 'n': int(len(accel_pre_list))}
    r, p = pearsonr(accel_pre_list, asinc_next_list)
    return {'CPT_r': round(float(r), 4), 'CPT_p': round(float(p), 4),
            'n': int(len(accel_pre_list))}

