    _peaks_por_tramo_muestras = peaks_por_tramo   # dict con arrays en muestras

    # ==========================================================================
    # 12. DETECCIÓN DE PICOS TIP — ALGORITMO DE FRAN [V5-1, V5-2]
    # ==========================================================================
    bpm_ref_video  = metadata.get('bpm', metadata.get('bpm_1', BPM_BASE))
    distance_video = int(fps * (60.0 / bpm_ref_video * 0.6))
    senal_y        = df['tip_y'].values
    prom_dinamica  = max(0.002, np.std(senal_y) * 0.4)
    print(f"\nDetección dinámica → Distancia: {distance_video} frames | Prominencia: {prom_dinamica:.4f}")

    tip_peaks_a = detect_peaks(-senal_y, prominence=prom_dinamica, distance=distance_video)
    tip_peaks_b, pico_bajo_inicial_idx = _construir_tip_peaks_b(
        df, tip_peaks_a, fps, bpm_ref_video, n_local=5)

    print(f"Picos altos en el aire (tip_peaks_a): {len(tip_peaks_a)}")
    print(f"Picos bajos en la mesa (tip_peaks_b): {len(tip_peaks_b)}")

    # [V5-8] Informe de sincronización
    if tipo == 'ISO':
        print(f"Se detectaron {len(tip_peaks_a)} picos altos en la punta de los dedos")
        print(f"Se detectaron {len(tip_peaks_b)} picos bajos en la punta de los dedos")
    elif tipo == 'PA':
        total_clics = metadata['clics_1'] + metadata['clics_2']
        print(f"Clics esperados: {total_clics} | Picos: {len(tip_peaks_a)} | "
              f"No sincronizados: {total_clics - len(tip_peaks_a)}")
    elif tipo == 'PR':
        total_clics = metadata['clics_1'] + metadata['clics_rampa'] + metadata['clics_2']
        print(f"Clics esperados: {total_clics} | Picos: {len(tip_peaks_a)} | "
              f"No sincronizados: {total_clics - len(tip_peaks_a)}")

