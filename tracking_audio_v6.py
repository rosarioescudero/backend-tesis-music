def _calcular_tiempos_apoyo_alzada(df, tip_peaks_a, tip_peaks_b, fps,
                                    savgol_win=11, savgol_poly=5, min_dur_ms=30):
    col='tip_y'
    umbral_bajo=np.max(df[col].iloc[tip_peaks_b].values)
    umbral_alto=np.min(df[col].iloc[tip_peaks_a].values)
    min_frames=max(1,int(np.ceil(min_dur_ms/1000.0*fps)))
    y=df[col].ffill().bfill().to_numpy()
    wl=savgol_win if len(y)>=savgol_win else (len(y) if len(y)%2==1 else len(y)-1)
    y_s=savgol_filter(y,wl,savgol_poly)
    en_apoyo=np.zeros(len(y_s),bool); alzada=np.zeros(len(y_s),bool)
    bajo=alto=False
    for i,v in enumerate(y_s):
        if   v>=umbral_bajo: bajo=True;  alto=False
        elif v<=umbral_alto: bajo=False; alto=True
        else:                bajo=False; alto=False
        en_apoyo[i]=bajo; alzada[i]=alto
    en_apoyo=binary_closing(en_apoyo,np.ones(min_frames))
    alzada=binary_closing(alzada,np.ones(min_frames))
    def _dur(mask):
        lbl,n=label(mask); durs=[]
        for sid in range(1,n+1):
            idx=np.where(lbl==sid)[0]
            if len(idx)>=min_frames: durs.append(len(idx)/fps*1000)
        return float(np.mean(durs)) if durs else 0.0
    t_a=_dur(en_apoyo); t_z=_dur(alzada)
    print(f"\nApoyo: {t_a:.1f} ms | Alzada: {t_z:.1f} ms")
    return t_a, t_z


