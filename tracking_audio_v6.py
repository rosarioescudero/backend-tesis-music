def _extraer_metadata(filename):
    """Extrae metadatos del nombre. Acepta '+', '-', '_' y guion medio."""
    m = re.search(r'(S\d+)_([A-Z]+)_ISO(\d+)[_-]([A-Za-z]+\d+)', filename)
    if m:
        return {'sujeto': re.sub(r'\D','',m.group(1)), 'mano': m.group(2),
                'tipo_metronomo': 'ISO', 'bpm': int(m.group(3)),
                'medicion': re.sub(r'\D','',m.group(4))}

    m = re.search(r'(S\d+)_([A-Z]+)_(PA)([+\-_]\d+)__(\d+)-(\d+)[_-]([A-Za-z]+\d+)', filename)
    if m:
        pct = int(m.group(4).replace('_','+') if m.group(4).startswith('_') else m.group(4))
        return {'sujeto': re.sub(r'\D','',m.group(1)), 'mano': m.group(2),
                'tipo_metronomo': 'PA', 'porcentaje_cambio': pct,
                'bpm_1': BPM_BASE, 'bpm_2': round(BPM_BASE*(1+pct/100.0)),
                'clics_1': int(m.group(5)), 'clics_2': int(m.group(6)),
                'medicion': re.sub(r'\D','',m.group(7))}

    m = re.search(r'(S\d+)_([A-Z]+)_(PR)([+\-_]\d+)__(\d+)-(\d+)-(\d+)[_-]([A-Za-z]+\d+)', filename)
    if m:
        pct = int(m.group(4).replace('_','+') if m.group(4).startswith('_') else m.group(4))
        return {'sujeto': re.sub(r'\D','',m.group(1)), 'mano': m.group(2),
                'tipo_metronomo': 'PR', 'porcentaje_cambio': pct,
                'bpm_1': BPM_BASE, 'bpm_2': round(BPM_BASE*(1+pct/100.0)),
                'clics_1': int(m.group(5)), 'clics_rampa': int(m.group(6)),
                'clics_2': int(m.group(7)),
                'medicion': re.sub(r'\D','',m.group(8))}
    return {}


def find_rhythmic_segment(audio_data, sample_rate, metadata,
                           back_search_s=2.0, smooth_ms=10, min_run_peaks=3):
    """Detección robusta de inicio y fin del patrón rítmico con valid_runs."""
    audio = np.asarray(audio_data)
    if audio.ndim > 1: audio = np.mean(audio, axis=1)
    max_abs = np.max(np.abs(audio))
    if max_abs == 0 or audio.size == 0: return 0, len(audio_data)
    audio_n    = audio / max_abs
    smooth_win = max(1, int(sample_rate * smooth_ms / 1000.0))
    env        = np.convolve(np.abs(audio_n), np.ones(smooth_win)/smooth_win, mode='same')

    bpm_r  = metadata.get('bpm', metadata.get('bpm_1', BPM_BASE))
    period = int(round(sample_rate * 60.0 / float(bpm_r)))
    dist   = max(1, int(0.5 * period))
    tol    = max(int(0.40 * period), 1)

    h    = np.percentile(env, 70)
    prom = 0.15 * np.max(env)
    peaks, _ = find_peaks(env, height=h, distance=dist, prominence=prom)
    for pct in (60, 50, 40):
        if len(peaks) >= min_run_peaks: break
        h    = np.percentile(env, pct)
        prom = max(0.05*np.max(env), 0.1*prom)
        peaks, _ = find_peaks(env, height=h, distance=dist, prominence=prom)

    if len(peaks) < min_run_peaks: return 0, len(audio_data)
    peaks = np.sort(peaks)

    diffs = np.diff(peaks)
    valid_runs = []; run_start = run_len = None
    for i, d in enumerate(diffs):
        if abs(d - period) <= tol:
            if run_start is None: run_start = i; run_len = 0
            run_len += 1
        else:
            if run_start is not None and run_len >= min_run_peaks:
                valid_runs.append((run_len, run_start))
            run_start = run_len = None
    if run_start is not None and run_len >= min_run_peaks:
        valid_runs.append((run_len, run_start))

    best_start = None
    if valid_runs:
        valid_runs.sort(key=lambda x: x[1])
        max_len = max(r[0] for r in valid_runs)
        for r_len, r_start in valid_runs:
            if r_len >= max_len * 0.3: best_start = r_start; break

    inicio_ritmo = peaks[best_start] if best_start is not None else peaks[0]
    seg_s = max(0, inicio_ritmo - int(back_search_s * sample_rate))
    seg   = env[seg_s: inicio_ritmo + 1]
    if seg.size > 0:
        ps, _ = find_peaks(seg, height=np.percentile(seg, 60),
                           distance=max(1, int(0.3 * period)))
        if len(ps) > 0: inicio_ritmo = int(seg_s + ps[-1])

    tipo = metadata.get('tipo_metronomo', 'ISO'); fin_ritmo = peaks[-1]
    if tipo in ('PR', 'PA'):
        try:
            dur_1 = metadata['clics_1'] * (60.0 / metadata['bpm_1'])
            dur_2 = metadata['clics_2'] * (60.0 / metadata['bpm_2'])
            dur_r = _calcular_tiempo_rampa(metadata['bpm_1'], metadata['bpm_2'],
                                           metadata.get('clics_rampa', 0)) if tipo=='PR' else 0.0
            fin_min = inicio_ritmo + int((dur_1 + dur_r + dur_2) * sample_rate)
            ventana = int(2.5 * (60.0/min(metadata['bpm_1'],metadata['bpm_2'])) * sample_rate)
            candidatos = peaks[(peaks >= fin_min) & (peaks <= fin_min + ventana)]
            fin_ritmo  = int(candidatos[-1]) if len(candidatos) else fin_min
        except KeyError as e:
            print(f"Advertencia fin_ritmo: {e}")
    else:
        is_r = np.abs(np.diff(peaks) - period) <= tol
        req  = min_run_peaks - 1
        cons = np.convolve(is_r, np.ones(req, int), mode='valid') == req
        idxs = np.where(cons)[0]
        if len(idxs) > 0: fin_ritmo = int(peaks[idxs[-1] + req])

    fin_ritmo = min(len(env) - 1, fin_ritmo + period // 2)

    if GRAFICAR:
        fig, ax = plt.subplots(figsize=(15, 4))
        ax.plot(env, color='gray', label='Envolvente')
        ax.scatter(peaks, env[peaks], color='red', s=20, label='Picos')
        ax.axvline(inicio_ritmo, color='g', linestyle='--',
                   label=f'Inicio ({inicio_ritmo/sample_rate:.2f}s)')
        ax.axvline(fin_ritmo, color='orange', linestyle='--',
                   label=f'Fin ({fin_ritmo/sample_rate:.2f}s)')
        ax.set_xlabel('Muestras'); ax.set_ylabel('Amplitud')
        ax.set_title('Segmento rítmico')
        ax.legend(); ax.grid(True); plt.tight_layout(); _mostrar_grafico()

    return int(inicio_ritmo), int(fin_ritmo)


def calcular_centroide_tip(df, pesos=None):
    tips  = ['indice_tip', 'medio_tip', 'anular_tip', 'meñique_tip']
    if pesos is None:
        pesos = {'indice_tip':0.4,'medio_tip':0.4,'anular_tip':0.1,'meñique_tip':0.1}
    total = sum(pesos.values())
    df['promedio_tip_x'] = sum(df[f'{t}_x']*pesos[t] for t in tips) / total
    df['promedio_tip_y'] = sum(df[f'{t}_y']*pesos[t] for t in tips) / total
    return df


def calcular_angulo_postura(df):
    mcps = ['indice_mcp','medio_mcp','anular_mcp','meñique_mcp']
    df['promedio_mcp_x'] = df[[f'{m}_x' for m in mcps]].mean(axis=1)
    df['promedio_mcp_y'] = df[[f'{m}_y' for m in mcps]].mean(axis=1)
    Ax = df['promedio_tip_x'] - df['promedio_mcp_x']
    Ay = df['promedio_tip_y'] - df['promedio_mcp_y']
    Bx = df['promedio_mcp_x'] - df['muñeca_x']
    By = df['promedio_mcp_y'] - df['muñeca_y']
    dot   = Ax*Bx + Ay*By
    magA  = np.sqrt(Ax**2 + Ay**2); magB = np.sqrt(Bx**2 + By**2)
    denom = np.where(magA*magB == 0, 1e-10, magA*magB)
    df['angulo_postura'] = np.degrees(np.arccos(np.clip(dot/denom, -1.0, 1.0)))
    return df


def generar_senal_ideal(df, metadata, fps):
    """[C-6.1] Sinusoide pura en Y calibrada sobre std(tip_y)."""
    tipo = metadata.get('tipo_metronomo','ISO')
    n = len(df); t = np.arange(n)/fps
    A = df['tip_y'].std(); offset = df['tip_y'].mean()
    if tipo == 'ISO':
        ideal_y = offset + A * np.sin(2*np.pi*(metadata['bpm']/60.0)*t)
    elif tipo == 'PA':
        bpm_1, bpm_2 = metadata['bpm_1'], metadata['bpm_2']
        t_c = metadata['clics_1']*(60.0/bpm_1)
        ideal_y = np.where(t <= t_c,
                           offset + A*np.sin(2*np.pi*(bpm_1/60.0)*t),
                           offset + A*np.sin(2*np.pi*(bpm_2/60.0)*t))
    elif tipo == 'PR':
        bpm_1, bpm_2 = metadata['bpm_1'], metadata['bpm_2']
        dur_1  = metadata['clics_1']*(60.0/bpm_1)
        dur_r  = _calcular_tiempo_rampa(bpm_1, bpm_2, metadata['clics_rampa'])
        t_r_f  = dur_1 + dur_r
        bpm_i  = np.where(t <= dur_1, bpm_1,
                 np.where(t <= t_r_f,
                          bpm_1 + (bpm_2-bpm_1)*(t-dur_1)/(t_r_f-dur_1), bpm_2))
        ideal_y = offset + A*np.sin(np.cumsum(2*np.pi*(bpm_i/60.0)/fps))
    else:
        ideal_y = np.full(n, offset)
    df['ideal_y'] = ideal_y; df['ideal_x'] = df['tip_x'].mean()
    return df


def calcular_metricas_movimiento(df, landmark_name, fps, window_length=9, polyorder=3):
    """[C-8.2] Suavidad, armonía y curvatura. No modifica el DataFrame."""
    xc, yc = f'{landmark_name}_x', f'{landmark_name}_y'
    if xc not in df.columns or yc not in df.columns: return np.nan, np.nan, np.nan
    wl = int(window_length)
    if wl%2==0: wl+=1
    if wl<=polyorder: wl=polyorder+2
    x  = df[xc].ffill().bfill().values; y = df[yc].ffill().bfill().values
    vx = savgol_filter(x, wl, polyorder, deriv=1, delta=1/fps)
    vy = savgol_filter(y, wl, polyorder, deriv=1, delta=1/fps)
    ax_= savgol_filter(x, wl, polyorder, deriv=2, delta=1/fps)
    ay_= savgol_filter(y, wl, polyorder, deriv=2, delta=1/fps)
    jx = savgol_filter(x, wl, polyorder, deriv=3, delta=1/fps)
    jy = savgol_filter(y, wl, polyorder, deriv=3, delta=1/fps)
    smoothness = float(np.sqrt(ax_**2+ay_**2).std())
    harmony    = float(np.sqrt(jx**2+jy**2).std())
    speed = np.sqrt(vx**2+vy**2); mask = speed>1e-6
    curv  = np.zeros(len(x))
    curv[mask] = np.abs(vx[mask]*ay_[mask]-vy[mask]*ax_[mask]) / speed[mask]**3
    return smoothness, harmony, float(np.mean(curv))


def _calcular_amplitudes_por_tramo(df, tip_peaks_a, tip_peaks_b, metadata, fps, cambios):
    tipo  = metadata.get('tipo_metronomo','ISO'); y_cm = df['tip_y_cm']; resultados = []
    def _amp(ini_s, fin_s, lbl):
        ini_f, fin_f = int(ini_s*fps), int(fin_s*fps); amps = []
        for pa in tip_peaks_a:
            if not (ini_f<=pa<=fin_f): continue
            bajos = tip_peaks_b[(tip_peaks_b>pa)&(tip_peaks_b<=fin_f)]
            if len(bajos): amps.append(abs(y_cm.iloc[bajos[0]]-y_cm.iloc[pa]))
        media = float(np.mean(amps)) if amps else 0.0
        print(f"  Amplitud {lbl}: {media:.2f} cm")
        return {'label':lbl,'amplitud_media_cm':round(media,3)}
    if tipo=='ISO': resultados.append(_amp(0,len(df)/fps,'ISO'))
    elif tipo=='PA':
        c=cambios
        resultados+=[_amp(0,c,f"{metadata['bpm_1']} bpm"),_amp(c,len(df)/fps,f"{metadata['bpm_2']} bpm")]
    elif tipo=='PR':
        c1,c2=cambios
        resultados+=[_amp(0,c1,f"{metadata['bpm_1']} bpm"),_amp(c1,c2,"Rampa"),_amp(c2,len(df)/fps,f"{metadata['bpm_2']} bpm")]
    return resultados


def _calcular_tiempos_ascenso_descenso(df, tip_peaks_a, tip_peaks_b, metadata, fps, cambios):
    tipo=metadata.get('tipo_metronomo','ISO'); resultados=[]
    def _t(ini_s,fin_s,lbl):
        ini_f,fin_f=int(ini_s*fps),int(fin_s*fps)
        pa_t=tip_peaks_a[(tip_peaks_a>=ini_f)&(tip_peaks_a<=fin_f)]
        pb_t=tip_peaks_b[(tip_peaks_b>=ini_f)&(tip_peaks_b<=fin_f)]
        desc,asc=[],[]
        for pa in pa_t:
            pb=pb_t[pb_t>pa]
            if len(pb): desc.append((pb[0]-pa)*1000.0/fps)
        for pb in pb_t:
            pa=pa_t[pa_t>pb]
            if len(pa): asc.append((pa[0]-pb)*1000.0/fps)
        td=float(np.mean(desc)) if desc else np.nan
        ta=float(np.mean(asc))  if asc  else np.nan
        suma=(td+ta) if not (np.isnan(td) or np.isnan(ta)) else np.nan
        print(f"  {lbl} — Descenso: {td:.1f} ms | Ascenso: {ta:.1f} ms")
        return {'label':lbl,'t_desc_ms':round(td,2),'t_asc_ms':round(ta,2),
                'pct_desc':round(td/suma*100,2) if suma else None,
                'pct_asc':round(ta/suma*100,2) if suma else None}
    if tipo=='ISO': resultados.append(_t(0,len(df)/fps,'ISO'))
    elif tipo=='PA':
        c=cambios
        resultados+=[_t(0,c,f"{metadata['bpm_1']} bpm"),_t(c,len(df)/fps,f"{metadata['bpm_2']} bpm")]
    elif tipo=='PR':
        c1,c2=cambios
        resultados+=[_t(0,c1,f"{metadata['bpm_1']} bpm"),_t(c1,c2,"Rampa"),_t(c2,len(df)/fps,f"{metadata['bpm_2']} bpm")]
    return resultados


def _calcular_tiempos_asc_desc_serie(tip_peaks_a, tip_peaks_b, fps):
    desc,asc=[],[]
    for pa in tip_peaks_a:
        pb=tip_peaks_b[tip_peaks_b>pa]
        if len(pb): desc.append((pb[0]-pa)*1000.0/fps)
    for pb in tip_peaks_b:
        pa=tip_peaks_a[tip_peaks_a>pb]
        if len(pa): asc.append((pa[0]-pb)*1000.0/fps)
    return np.array(desc), np.array(asc)


