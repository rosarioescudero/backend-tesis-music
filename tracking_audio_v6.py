def graficar_validacion_inversion(stim_times, stim_times_iso, resp_times,
                                   resp_times_iso, metadata, filename_stem):
    """
    [V6-3] Valida que revert_to_original sea biyectiva.
    Scatter orig vs revertido (debe caer sobre y=x) + error a lo largo del tiempo.
    """
    resp_times            = np.asarray(resp_times)
    resp_times_iso        = np.asarray(resp_times_iso)
    resp_times_revertidos = revert_to_original(resp_times_iso, stim_times_iso, stim_times)

    error_abs   = np.abs(resp_times - resp_times_revertidos)
    error_max   = np.max(error_abs)
    error_medio = np.mean(error_abs)

    fig, axes = plt.subplots(1, 2, figsize=(14, 5))

    # Scatter orig vs revertido
    ax = axes[0]
    ax.scatter(resp_times, resp_times_revertidos, alpha=0.6, color='steelblue', s=30)
    lims = [min(resp_times.min(), resp_times_revertidos.min()),
            max(resp_times.max(), resp_times_revertidos.max())]
    ax.plot(lims, lims, 'r--', linewidth=1.5, label='Identidad ideal (y = x)')
    ax.set_title(f'Validación de Inversión\n'
                 f'Error medio: {error_medio*1000:.3f} ms  |  Error máx: {error_max*1000:.3f} ms')
    ax.set_xlabel('Tiempo original [s]'); ax.set_ylabel('Tiempo revertido desde ISO [s]')
    ax.legend(fontsize=9); ax.grid(True, linestyle=':', alpha=0.5); ax.set_aspect('equal', adjustable='box')

    # Error a lo largo del tiempo
    ax = axes[1]
    ax.plot(resp_times, error_abs * 1000, 'o-', color='darkorange', markersize=4, linewidth=1)
    ax.axhline(y=error_medio * 1000, color='red', linestyle='--', linewidth=1.2,
               label=f'Error medio ({error_medio*1000:.3f} ms)')
    ax.fill_between(resp_times, 0, error_abs * 1000, alpha=0.15, color='darkorange')
    graficar_cambios_bpm(metadata, 0, ax=ax)
    ax.set_title('Error de inversión a lo largo de la sesión')
    ax.set_xlabel('Tiempo original [s]'); ax.set_ylabel('|original − revertido| [ms]')
    ax.legend(fontsize=9); ax.grid(True, linestyle=':', alpha=0.5)

    plt.suptitle(f'Validación Transformación Inversa — {filename_stem}',
                 fontsize=12, fontweight='bold')
    plt.tight_layout()

    nombre = f"{filename_stem}_validacion_inversion.png"
    _guardar_y_mostrar(nombre)

    # Reporte consola
    print(f"  Error medio de inversión: {error_medio*1000:.4f} ms | Error máx: {error_max*1000:.4f} ms")
    if error_max < 0.001:   print("  ✅ Inversión perfecta (error < 1 µs).")
    elif error_max < 0.010: print("  ✅ Inversión excelente (error < 10 µs).")
    else:                   print("  ⚠️  Error notable. Revisar extrapolaciones o anclas.")
    return nombre


