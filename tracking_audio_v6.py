def emparejar_tiempos(arr_ref, arr_comp, ventana_s=0.15):
    """[N-3] Matching greedy entre dos arrays de tiempos dentro de una ventana."""
    arr_comp_temp = list(arr_comp)
    diffs_ms, ref_matched, comp_matched = [], [], []
    for t_ref in arr_ref:
        if not arr_comp_temp: break
        dists = np.abs(np.array(arr_comp_temp) - t_ref)
        idx   = np.argmin(dists)
        if dists[idx] <= ventana_s:
            t_comp = arr_comp_temp.pop(idx)
            diffs_ms.append((t_comp - t_ref) * 1000.0)
            ref_matched.append(t_ref); comp_matched.append(t_comp)
    return np.array(diffs_ms), np.array(ref_matched), np.array(comp_matched)


def calcular_repetidos_tramo(tapping_peaks, bpm, sample_rate):
    """[N-10] Tappeos dobles: pares de picos separados menos de ISI/2."""
    if len(tapping_peaks) <= 1: return 0
    return int(np.sum(np.diff(tapping_peaks) < (60.0 / bpm / 2.0) * sample_rate))


